def main() -> None:
    if SAMPLE_DIR.exists():
        shutil.rmtree(SAMPLE_DIR)
    SAMPLE_DIR.mkdir(parents=True, exist_ok=True)

    metadata = []
    for case in CASES:
        text = scheme_text(case)
        if len(text) < 3000:
            raise ValueError(f"{case['file']} too short: {len(text)}")
        path = SAMPLE_DIR / case["file"]
        path.write_text(text, encoding="utf-8")
        metadata.append(
            {
                "file": case["file"],
                "title": case["title"],
                "expected_main": case["expected_main"],
                "forbidden_main": case["forbidden_main"],
            }
        )

    METADATA_PATH.write_text(json.dumps(metadata, ensure_ascii=False, indent=2), encoding="utf-8")
    print(f"GENERATED {len(metadata)} deep regression samples -> {SAMPLE_DIR}")


if __name__ == "__main__":
    main()
