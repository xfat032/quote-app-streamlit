def make_content(theme: str, name: str, cues: list[str], audience: str) -> str:
    cue_text = "、".join(cues)
    return (
        f"围绕“{theme}”设置{name}，现场将{cue_text}串联成完整执行流程。参与者先根据导视指引进入对应区域，"
        f"再由工作人员或主持人说明参与方式，最后完成互动、观看、体验、领取或复盘等动作。该环节不是简单摆放物料，"
        f"而是需要结合场地动线、人员分工、设备调试、物料发放、摄影记录和安全提示同步执行，确保{audience}能够理解规则、"
        f"愿意停留、方便拍照传播，并在活动结束后留下可复盘的数据线索。具体数量、规格、服务时长和执行口径均需人工确认。"
    )


def activity_block(index: int, name: str, time: str, place: str, content: str, numbering: str = "number") -> str:
    if len(content) < 100:
        raise ValueError(f"{name} content too short: {len(content)}")
    cn_nums = "一二三四五六七八九十"
    heading = f"（{cn_nums[index - 1]}）{name}" if numbering == "paren" else f"{index}. {name}"
    return f"{heading}\n（1）时间：{time}\n（2）地点：{place}\n（3）内容：{content}"


def scheme_text(case: dict) -> str:
    blocks = "\n\n".join(
        activity_block(
            index,
            block["name"],
            block["time"],
            case["location"],
            make_content(case["theme"], block["name"], block["cues"], case["audience"]),
            str(case.get("numbering", "number")),
        )
        for index, block in enumerate(case["blocks"], start=1)
    )
    return "\n\n".join(
        [
            case["title"],
            COMMON_BACKGROUND.strip(),
            OVERVIEW.format(**case).strip(),
            f"四、{case.get('content_heading', '活动内容')}\n" + blocks,
            EXECUTION_NOTE.strip(),
            PROPAGATION.strip(),
        ]
    )


