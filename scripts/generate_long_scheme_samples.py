def block_text(index: int, name: str, time: str, body: str, subitems: list[tuple[str, str]]) -> str:
    cn_nums = "一二三四五六七八九十"
    marker = cn_nums[index - 1]
    parts = [
        f"（{marker}）板块{marker}：{name}",
        f"时间：{time}",
        body.strip(),
    ]
    for item_index, (title, content) in enumerate(subitems, start=1):
        parts.append(f"{item_index}.{title}：{content.strip()}")
    return "\n".join(parts)


def extension_text(title: str, items: list[tuple[str, str]]) -> str:
    parts = [
        f"六、延展活动：{title}",
        "（一）基本信息",
        "延展活动作为主活动后的轻量化持续运营内容，原则上沿用主视觉系统和互动机制，但缩小场地范围、降低搭建体量、提高周末常态化执行效率。",
        "（二）活动定位",
        "延展活动承担持续引流、社群维护和内容复用功能，不追求一次性大规模声量，而强调可连续举办、可低成本复制、可与商户和公共空间长期协同。",
        "（三）活动内容规划",
    ]
    for index, (name, content) in enumerate(items, start=1):
        parts.append(f"{index}、{name}\n{content.strip()}")
    parts.append(
        "（四）项目作用\n"
        "1、延续主活动的内容表达，在不同周末形成持续触达。\n"
        "2、以轻量化形式测试可复制的城市常态活动模型。\n"
        "3、拓展活动的时间与空间维度，增强品牌持续性。\n"
        "4、丰富夜间文化消费内容，提升公共空间活力。"
    )
    return "\n".join(parts)


def make_case(
    *,
    filename: str,
    title: str,
    slogan: str,
    date: str,
    location: str,
    organizer: str,
    tone: str,
    intro: str,
    blocks: list[dict[str, object]],
    extension_title: str,
    extension_items: list[tuple[str, str]],
    expected_main: list[str],
    forbidden_main: list[str],
) -> dict[str, object]:
    main_content = [
        "四、主活动内容",
        intro.strip(),
    ]
    for index, block in enumerate(blocks, start=1):
        main_content.append(
            block_text(
                index,
                str(block["name"]),
                str(block["time"]),
                str(block["body"]),
                list(block["subitems"]),  # type: ignore[arg-type]
            )
        )
    text = "\n\n".join(
        [
            title,
            "一、活动背景",
            COMMON_BACKGROUND.strip(),
            OVERVIEW_TEMPLATE.format(
                title=title,
                slogan=slogan,
                date=date,
                location=location,
                organizer=organizer,
                tone=tone,
            ).strip(),
            STYLE_SECTION.strip(),
            "\n\n".join(main_content),
            PROPAGATION_SECTION.strip(),
            extension_text(extension_title, extension_items),
            SUPPORT_SECTION.strip(),
        ]
    )
    return {
        "filename": filename,
        "text": text,
        "expected_main": expected_main,
        "forbidden_main": forbidden_main,
    }


